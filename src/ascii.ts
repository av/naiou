export interface AsciiArt {
  lines: string[];
  width: number;
  height: number;
}

export function renderAsciiText(text: string, maxWidth: number, maxHeight: number): AsciiArt {
  const rows: string[][] = [[]];

  for (const char of text.toUpperCase()) {
    const glyph = FONT[char] ?? FONT["?"]!;
    const row = rows[rows.length - 1]!;
    const currentWidth = row.length === 0 ? 0 : row[0]!.length;
    const glyphWidth = glyph[0]?.length ?? 0;

    if (currentWidth > 0 && currentWidth + glyphWidth + 1 > maxWidth && rows.length * 6 < maxHeight) {
      rows.push([...glyph]);
      continue;
    }

    if (row.length === 0) {
      row.push(...glyph);
      continue;
    }

    for (let i = 0; i < 5; i++) {
      row[i] = `${row[i]} ${glyph[i] ?? ""}`;
    }
  }

  const lines = rows.flatMap((row, index) => index === 0 ? row : ["", ...row]).slice(0, maxHeight);
  return {
    lines,
    width: lines.reduce((max, line) => Math.max(max, line.length), 0),
    height: lines.length,
  };
}

export function drawAsciiText(
  buffer: OptimizedBuffer,
  art: AsciiArt,
  x: number,
  y: number,
  fg: RGBA,
  bg: RGBA,
) {
  for (let row = 0; row < art.lines.length; row++) {
    const line = art.lines[row]!;
    for (let col = 0; col < line.length; col++) {
      const ch = line[col] === " " ? " " : "‚Ėą";
      buffer.setCell(x + col, y + row, ch, fg, bg);
    }
  }
}
